/** \brief Suma dos numeros flotantes
 *
 * \param Primer numero
 * \param Segundo numero
 * \return Resultado de la suma de ambos numeros
 *
 */
float sumarFloat (float, float);


/** \brief Resta un numero flotante a otro
 *
 * \param Minuendo
 * \param Sustraendo
 * \return Resultado de la resta
 *
 */
float restarFloat (float, float);


/** \brief Castea y divide dos numeros flotantes
 *
 * \param Dividendo
 * \param Divisor
 * \return Resultado exacto de la division, con decimales
 *
 */
float dividirFloat (float, float);


/** \brief Multiplica dos numeros flotantes
 *
 * \param Multiplicando
 * \param Multiplicador
 * \return Resultado de la multiplicacion
 *
 */
float multiplicarFloat (float, float);


/** \brief Factoriza un numero entero
 *
 * \param Numero entero a factorizar
 * \return Factorial
 *
 */
int sacarFactorial (float);
